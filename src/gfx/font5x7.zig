//! font5x7.zig — Minimal bitmap font for overlay text.
//!
//! Each character is 5 pixels wide × 7 pixels tall, stored as 7 bytes
//! (one byte per row, MSB-first). Covers printable ASCII (0x20–0x7E).
//!
//! ## Memory Usage
//! 95 characters × 7 bytes = 665 bytes. Stored in .rodata (flash on ESP32),
//! not SRAM.
//!
//! ## Learning Notes
//! Bitmap fonts were universal before TrueType (1991). Each character is
//! a tiny 1-bit image. Rendering is just bit-testing in a loop — no
//! rasterization, no anti-aliasing, just on/off pixels. Perfect for
//! embedded displays.

const Framebuffer = @import("framebuffer.zig").Framebuffer;

/// Character width in pixels.
pub const CHAR_W: u16 = 5;

/// Character height in pixels.
pub const CHAR_H: u16 = 7;

/// Spacing between characters.
pub const CHAR_SPACING: u16 = 1;

/// Draw a single character at (x, y) in the given color.
///
/// Characters are drawn top-left aligned. Out-of-bounds pixels are clipped.
pub fn drawChar(fb: *Framebuffer, x: i16, y: i16, ch: u8, color: u16) void {
    if (ch < 0x20 or ch > 0x7E) return;
    const idx: usize = ch - 0x20;
    const glyph = FONT_DATA[idx];

    for (0..CHAR_H) |row| {
        const bits = glyph[row];
        for (0..CHAR_W) |col| {
            if (bits & (@as(u8, 0x80) >> @intCast(col)) != 0) {
                const px = x + @as(i16, @intCast(col));
                const py = y + @as(i16, @intCast(row));
                if (px >= 0 and py >= 0) {
                    fb.setPixel(@intCast(px), @intCast(py), color);
                }
            }
        }
    }
}

/// Draw a string at (x, y). Characters are spaced CHAR_W + CHAR_SPACING apart.
pub fn drawString(fb: *Framebuffer, x: i16, y: i16, str: []const u8, color: u16) void {
    var cx = x;
    for (str) |ch| {
        drawChar(fb, cx, y, ch, color);
        cx += @as(i16, CHAR_W + CHAR_SPACING);
    }
}

